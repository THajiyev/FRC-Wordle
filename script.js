const emojis = {
    "-1":" ",
    "0":" ",
    "1":" "
}

const light_green = '#90EE90';
const light_red = '#FFCCCC';

var attempts = 0;
var guesses = [];

function shakeInput() {
    const input = document.getElementById('teamInput');
    input.classList.add('shake');
    setTimeout(function() {
      input.classList.remove('shake');
    }, 300);
}

function validGuess(guess) {
    return frc_teams.hasOwnProperty(guess) && !guesses.includes(guess);
}

function chooseTeam() {
    const keys = Object.keys(frc_teams);
    const randomIndex = Math.floor(Math.random() * keys.length);
    return keys[randomIndex];
}

var correctTeam = chooseTeam()
var rookie_year_correct = frc_teams[correctTeam].rookie_year
var location_correct = frc_teams[correctTeam].location;
var epa_correct = frc_teams[correctTeam].epa_rank;
var division_correct = frc_teams[correctTeam].division;

function addTableRow(teamData, cellColors) {
    const table = document.getElementById('dataTable');
    const tbody = table.querySelector('tbody');
    const newRow = document.createElement('tr');
    teamData.forEach((value, index) => {
      const cell = document.createElement('td');
      cell.textContent = value;
      if (cellColors && cellColors[index]) {
        cell.style.backgroundColor = cellColors[index];
      }
      newRow.appendChild(cell);
    });
    tbody.appendChild(newRow);
}
  
function compareIntegers(correct_team, guessed_team) {
    return correct_team<guessed_team ? -1 : correct_team>guessed_team ? 1 : 0;
}

function createPopupContainer() {
    const popupContainer = document.createElement('div');
    popupContainer.classList.add('popup-container');
    return popupContainer;
}
  
function createPopupContent() {
    const popupContent = document.createElement('div');
    popupContent.classList.add('popup-content');
    return popupContent;
}
  
function createPopupMessage(text) {
    const message = document.createElement('span');
    message.classList.add('popup-message');
    message.textContent = text;
    return message;
}
  
function createButton(text, onClick) {
    const button = document.createElement('button');
    button.textContent = text;
    button.addEventListener('click', onClick);
    return button;
}
  
function closePopup() {
    const popupContainer = document.querySelector('.popup-container');
    if (popupContainer) {
        popupContainer.remove();
    }
}
  
function showPopup(wordleCompleted) {
    const popupContainer = createPopupContainer();
    const popupContent = createPopupContent();
    let messageText;
    if (wordleCompleted) {
      messageText = 'Congratulations! You guessed the team in ' + attempts + (attempts > 1 ? ' tries' : ' try');
    } else {
      messageText = 'The correct answer was ' + correctTeam;
    }
    const message = createPopupMessage(messageText);
    const closeButton = createButton('Close', closePopup);
    const playAgainButton = createButton('Play Again', reloadPage);
    popupContent.appendChild(message);
    popupContent.appendChild(closeButton);
    popupContent.appendChild(playAgainButton);
    popupContainer.appendChild(popupContent);
    const guessButton = document.getElementById('teamInput');
    guessButton.disabled = true;
    document.body.appendChild(popupContainer);
}
  
function confirmRevealingAnswer() {
    const popupContainer = createPopupContainer();
    const popupContent = createPopupContent();
    const message = createPopupMessage('Are you sure you want to see the answer?');
    const noButton = createButton('No', closePopup);
    const yesButton = createButton('Yes', function() {
        closePopup();
        showPopup(false);
    });
    popupContent.appendChild(message);
    popupContent.appendChild(noButton);
    popupContent.appendChild(yesButton);
    popupContainer.appendChild(popupContent);
    document.body.appendChild(popupContainer);
}
  
function closePopup() {
    const popupContainer = document.querySelector('.popup-container');
    if (popupContainer) {
        popupContainer.remove();
    }
}

function reloadPage() {
    location.reload();
}

function guessAttempted(){
    var guess = document.getElementById('teamInput').value;
    if(!validGuess(guess)){
        shakeInput();
        return;
    }
    attempts+=1;
    guesses.push(guess);
    var rookie_year_guessed = frc_teams[guess].rookie_year
    var location_guessed = frc_teams[guess].location;
    var epa_guessed = frc_teams[guess].epa_rank;
    var division_guessed = frc_teams[guess].division;
    var location_matches = location_guessed==location_correct;
    var division_matches = division_guessed==division_correct;
    var younger_team = compareIntegers(rookie_year_correct, rookie_year_guessed);
    var epa_rank_higher = - compareIntegers(epa_correct, epa_guessed);
    addTableRow(
        [
            guess,
            division_guessed,
            location_guessed,
            epa_guessed+emojis[epa_rank_higher],
            rookie_year_guessed+emojis[younger_team]
        ],
        [
            null,
            division_matches?light_green:light_red,
            location_matches?light_green:light_red,
            null,
            null
        ]
    )
    document.getElementById('teamInput').value = "";
    if(division_matches && location_matches && younger_team==0 && epa_rank_higher==0){
        showPopup(true);
    }
}
